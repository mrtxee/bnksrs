    private double getSumAccounts(long[] agents) {
        double result = 0;
        for (long acc : agents) {
            AccountDto a = accountService.findByAccountNumber(acc);
            result += a.getAmount();
        }
        return result;
    }

    @Test
    void massTransactionsManyThreads() {
        final long[] agents = {4003000000000001L, 4003000000000002L, 4003000000000003L};
        double sum1 = getSumAccounts(agents);
        List<TransferMoneyTask> trTasks = getTrTasksList();
        ThreadPoolExecutor executor = (ThreadPoolExecutor) Executors.newFixedThreadPool(4);
        try {
            executor.invokeAll(trTasks);
        } catch (InterruptedException e) {
            throw new RuntimeException(e);
        }
        double sum2 = getSumAccounts(agents);

        System.out.printf("agents sum delta: %s ?= 0%n", sum1-sum2);
        assertThat(sum1-sum2).isEqualTo(0);
    }

